#include <visualizer.h>

void	draw_map()
{
	int		i;
	int		x;
	int		y;

	i = -1;
	while (++i < MEM_SIZE)
	{
		x = (i % 64);
		y = (i / 64);
		x = x + (13 * x) - SIDE;
		y = y - (15 * y) + SIDE;
		rectangle(x, y, x + 13, y - 13);
	}
}
